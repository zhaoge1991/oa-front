export class CommonActionBarConfig {
  addNewUrl:string;
  openUrl:string;
  idName:string;
  editUrl:string;
  isProcurementOrder:boolean;
  constructor(
    addNewUrl:string,
    openUrl:string,
    idName:string,
    editUrl:string,
    isProcurementOrder:boolean
  ){
    this.addNewUrl=addNewUrl
    this.openUrl=openUrl
    this.idName=idName
    this.editUrl=editUrl
    this.isProcurementOrder=isProcurementOrder
  }
}
